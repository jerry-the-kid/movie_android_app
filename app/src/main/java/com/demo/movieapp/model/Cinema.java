package com.demo.movieapp.model;

public class Cinema {
    String name;
    String city;

    public Cinema() {

    }

    public Cinema(String name, String city) {
        this.name = name;
        this.city = city;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
